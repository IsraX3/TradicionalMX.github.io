                            <h2 class="card-title">El Trompo</h2>
                            <p class="card-text"> El trompo es un juguete tradicional que consiste en un objeto en forma de cono fabricado típicamente de madera, aunque los hay de plástico o metal y cuenta con una punta afilada en la parte inferior que se apoya en el suelo para girar con ayuda de una cuerda. El objetivo de es lograr que gire sobre su punta, enrollándolo con una cuerda para lanzarlo al suelo con fuerza para que logre “bailar”, hacer que se equilibre sobre su propio eje.
<br><br>
¿Cómo se juega?
<br><br>
1.	Tendrás que enrollar la cuerda o hilo sobre el trompo desde la punta hasta la mitad de este.
2.	Toma el trompo con tu mano más ágil y sostenlo por la punta
3.	Revisa que haya espacio y no haya nada donde pueda golpear
4.	Avienta el trompo hacia el suelo, como si se tratara de una bola de béisbol, y jala la cuerda inmediatamente para que comience a girar el trompo
5.	Cuida que no vaya hacia algún objeto que pueda tirarlo
6.	Con ayuda de la cuerda, puedes subirlo a tu mano
</p>
