                            <h2 class="card-title">Balero</h2>
                            <p class="card-text">  
El origen de este tradicional juego de destreza es muy incierto. Existen registros mayas que datan del período Clásico (entre el año 250 y el 950), en los cuales se hace referencia a la práctica de un juego muy parecido. Es conocido y jugado en gran parte de América Latina.
<br><br>
¿Cómo se juega?
<br><br>
El juego del balero está conformado por una bola o barril pequeño de madera con un hueco en el centro y un pequeño palo, ambos elementos están unidos por un cordón.
Resulta uno de los juegos más queridos y populares del país por su alto nivel de destreza. El objetivo del juego es atrapar con el palo el pequeño barril de madera mientras gira en el aire. Pareciera trabajo fácil, pero requiere mucha práctica, paciencia y cuidado pues es usual llevarse uno que otro golpe en el intento. La forma del balero es diferente de acuerdo con la región en que se juega, hay variantes en la forma de la copa y el mango.

</p>
